fun main(){
    var myNum  = 5

    // Assignment Operators (+=, -=, *=, /=, %=)
    myNum += 3
    myNum -= 2
    myNum *= 3
    myNum /= 6
    myNum %= 2
    println("myNum is $myNum")

    //Increment & Decrement (++ , --)

    myNum++

    println(myNum)
    println(++myNum)
    println(myNum--)
    println(--myNum)
    println(myNum--)
    println(myNum)
}